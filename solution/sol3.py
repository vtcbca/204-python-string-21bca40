#WAS to enter any number and print appropreat day name.
num=int(input("Enter any number:"))
if(num==1):
    print("MONDAY")
elif(num==2):
    print("TUESDAY")
elif(num==3):
    print("WEDESDAY")
elif(num==4):
    print("THRUSDAY")
elif(num==5):
    print("FRIDAY")
elif(num==6):
    print("SATURDAY")
elif(num==7):
    print("SUNDAY")
else:
    print("INVALID NUMBER")
